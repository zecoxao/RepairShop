    public void switchPanels(JPanel jpOff, JPanel jp){
        jpOff.setVisible(false);
        //
        if (jp != null){
            jp.setVisible(true);
            this.getFrame().setContentPane(jp);
        }
        else{
            this.mainPanel.setVisible(true);
            this.getFrame().setContentPane(this.mainPanel);
        }
        this.getFrame().repaint();
    }

